package com.yogocodes.httpmonitor.core;

public class MonitorResultSummary {

	private String host;
	private long time;

	public String getHost() {
		return host;
	}

	public void setHost(final String host) {
		this.host = host;
	}

	public long getTime() {
		return time;
	}

	public void setTime(final long time) {
		this.time = time;
	}

}
